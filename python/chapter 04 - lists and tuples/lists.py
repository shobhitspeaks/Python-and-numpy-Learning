# lists:
name = ["Shobhit", "Lakshya", "Parth", 23 , 89.2344] 
# lists can store any data type values and lists are mutable
print(name[1])
name[1] = "Prachi Tripathi"
print(name)

# append function:
name.append("Suhani")
print(name)

# sort function:
numbers = [23,7913,5,928,111]
# numbers.sort()
print(numbers)

# reverse function:
numbers.reverse()
print(numbers)

# insert function:
numbers.insert(3,696996)
print(numbers)

# pop function:
name.pop(2)
print(name)